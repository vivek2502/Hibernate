package p1;

public class Book {
	private int bid;
	private String bname;
	private Publisher pid;
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public String getBname() {
		return bname;
	}
	public void setBname(String bname) {
		this.bname = bname;
	}
	public Publisher getPid() {
		return pid;
	}
	public void setPid(Publisher pid) {
		this.pid = pid;
	}
	
}
